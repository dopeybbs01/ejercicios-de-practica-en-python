"""
Script en python que le pida al usuario que ingrese dos numeros
y poteriormente que muestre la suma.

"""
print("Calculadora para sumar dos numeros\n")
number_one = int(input("Ingreese un valor\n"))
number_two = int(input("Ingrese un valor\n"))

sum_number = number_one + number_two

print(f"La suma es: {sum_number}")