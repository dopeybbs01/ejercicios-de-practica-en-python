"""
Script en python que calcule el area de un triangulo. 
El usuario debera ingresar tanto el valor de la base como
el de la altura y el prograna nos mostrara el valor del area.

"""
print("Calcular el area de un triangulo\n")
base = float(input("Base\n"))
height = float(input("altura\n"))

area = base * height / 2

print(f"El valor del area es: {area}")
