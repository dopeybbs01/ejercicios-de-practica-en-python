# Escriba un programa que pida dos números y que escriba su media aritmética.
# Se recuerda que la media aritmética de dos números es la suma de ambos números dividida por 2

print("CÁLCULO DE LA MEDIA DE DOS NÚMEROS")
number_one = float(input("Ingresa un numero: "))
number_two = float(input("Ingrese un numero: "))

number = (number_one + number_two) / 2

print(f"La media aritmeica de {number_one} y {number_two} es: {number}")
